// Cache for loaded patterns
let cachedPatterns: AtheonPattern[] | null = null;
let patternsLoadPromise: Promise<AtheonPattern[]> | null = null;

// Default bundle path - uses environment variable or sensible default
const DEFAULT_ATHEON_BUNDLE_PATH = process.env.ATHEON_BUNDLE_PATH ||
  (process.env.HOME ? `${process.env.HOME}/.atheon/patterns.bundle` : './patterns.bundle');

/**
 * Convert bundle pattern to AtheonPattern format
 */
function convertBundlePattern(bundlePattern: BundlePattern): AtheonPattern {
  // Map category to severity - use sensible defaults
  const categorySeverities: Record<string, 'critical' | 'high' | 'medium' | 'low'> = {
    'secrets': 'critical',
    'security': 'critical',
    'security-hardening': 'high',
    'code-quality': 'medium',
    'accessibility': 'high',
    'devops': 'medium',
    'ai-detection': 'low',
    'healthcare': 'high',
    'finance': 'high',
    'pii': 'critical',
    'frameworks': 'low',
    'performance': 'medium',
    'api-integration': 'medium',
    'cloud-native': 'medium',
    'data-visualization': 'low',
    'web-development': 'low',
    'web-security': 'high',
    'pwa': 'low',
  };

  return {
    name: bundlePattern.name,
    category: bundlePattern.category,
    severity: categorySeverities[bundlePattern.category] || 'medium',
    pattern: bundlePattern.match,
    description: `${bundlePattern.category}: ${bundlePattern.name} pattern`,
  };
}

/**
 * Load Atheon patterns from bundle or fallback to hardcoded patterns
 * Uses caching to avoid repeated file reads
 */
export async function loadAtheonPatterns(): Promise<AtheonPattern[]> {
  // Return cached patterns if available
  if (cachedPatterns !== null) {
    return cachedPatterns;
  }

  // If already loading, return the existing promise
  if (patternsLoadPromise !== null) {
    return patternsLoadPromise;
  }

  // Start loading
  patternsLoadPromise = (async () => {
    const bundlePath = process.env.ATHEON_BUNDLE_PATH || DEFAULT_ATHEON_BUNDLE_PATH;
    const bundlePatterns = await loadPatternsFromBundle(bundlePath);

    if (bundlePatterns.length > 0) {
      cachedPatterns = bundlePatterns.map(convertBundlePattern);
      console.log(`Loaded ${cachedPatterns.length} patterns from bundle`);
      return cachedPatterns;
    }

    // Fallback to hardcoded patterns
    console.log('Using fallback hardcoded patterns');
    cachedPatterns = FALLBACK_ATHEON_PATTERNS;
    return cachedPatterns;
  })();

  return patternsLoadPromise;
}

/**
 * Get ATHEON_PATTERNS - loads from bundle asynchronously
 * For synchronous access, use loadAtheonPatterns() first
 */
export let ATHEON_PATTERNS: AtheonPattern[] = FALLBACK_ATHEON_PATTERNS;

/**
 * Initialize ATHEON_PATTERNS from bundle (call this during app startup)
 */
export async function initializeAtheonPatterns(): Promise<void> {
  ATHEON_PATTERNS = await loadAtheonPatterns();
}

export class AtheonClaudeClient extends MCPClaudeClient {
  private atheonConfig: AtheonIntegrationConfig;
  private scanResults: Map<string, AtheonFinding[]> = new Map();

  constructor(config: AtheonIntegrationConfig) {
    super(config);
    this.atheonConfig = config;
  }

  /**
   * Execute with Atheon MCP tools and automatic scanning
   */
  async executeWithAtheon(
    messages: ClaudeMessage[],
    scanOutput: boolean = true
  ): Promise<{
    response: any;
    metrics: BenchmarkMetrics;
    toolCalls: MCPToolCall[];
    findings: AtheonFinding[];
    passedQualityGates: boolean;
  }> {
    // Get Atheon tools
    const atheonTools = this.getAtheonTools();

    // Execute with MCP support
    const { response, metrics, toolCalls } = await this.executeWithMCP(messages, atheonTools);

    // Scan output if enabled
    let findings: AtheonFinding[] = [];
    let passedQualityGates = true;

    if (scanOutput && this.atheonConfig.atheon.enabled) {
      const output = response.content
        .filter((block: any) => block.type === 'text')
        .map((block: any) => block.text)
        .join('\n');

      findings = await this.scanWithAtheon(output);

      if (this.atheonConfig.qualityGates?.enabled) {
        passedQualityGates = this.evaluateQualityGates(findings);
      }
    }

    return {
      response,
      metrics,
      toolCalls,
      findings,
      passedQualityGates,
    };
  }

  /**
   * Run benchmark with Atheon integration
   */
  async runBenchmarkWithAtheon(
    benchmarkId: string,
    testCaseId: string,
    testCaseName: string,
    prompt: string,
    systemPrompt?: string,
    expectedFindings?: number
  ): Promise<BenchmarkResult & {
    toolCalls: MCPToolCall[];
    findings: AtheonFinding[];
    passedQualityGates: boolean;
  }> {
    const messages: ClaudeMessage[] = [];

    if (systemPrompt) {
      messages.push({ role: 'system', content: systemPrompt });
    }

    messages.push({ role: 'user', content: prompt });

    try {
      const { response, metrics, toolCalls, findings, passedQualityGates } =
        await this.executeWithAtheon(messages);

      const output = response.content
        .filter((block: any) => block.type === 'text')
        .map((block: any) => block.text)
        .join('\n');

      // Store scan results
      this.scanResults.set(testCaseId, findings);

      return {
        benchmarkId,
        testCaseId,
        testCaseName,
        configuration: 'atheon',
        claudeVersion: this.atheonConfig.model || 'unknown',
        metrics,
        output,
        passed: passedQualityGates,
        toolCalls,
        findings,
        passedQualityGates,
      };
    } catch (error) {
      const errorMessage = error instanceof Error ? error.message : 'Unknown error';

      return {
        benchmarkId,
        testCaseId,
        testCaseName,
        configuration: 'atheon',
        claudeVersion: this.atheonConfig.model || 'unknown',
        metrics: {
          startTime: Date.now(),
          endTime: Date.now(),
          duration: 0,
          inputTokens: 0,
          outputTokens: 0,
          totalTokens: 0,
          success: false,
          errorMessage,
        },
        output: '',
        passed: false,
        error: errorMessage,
        toolCalls: [],
        findings: [],
        passedQualityGates: false,
      };
    }
  }

  /**
   * Scan text with Atheon patterns
   */
  async scanWithAtheon(text: string): Promise<AtheonFinding[]> {
    const findings: AtheonFinding[] = [];
    const lines = text.split('\n');

    // Filter patterns by configuration
    const enabledPatterns = ATHEON_PATTERNS.filter(pattern => {
      if (!this.atheonConfig.atheon.enabled) return false;

      const categories = this.atheonConfig.atheon.categories;
      const severity = this.atheonConfig.atheon.severity;

      if (categories && !categories.includes(pattern.category)) return false;
      if (severity && !severity.includes(pattern.severity)) return false;

      return true;
    });

    // Scan each line
    for (let lineNum = 0; lineNum < lines.length; lineNum++) {
      const line = lines[lineNum];

      for (const pattern of enabledPatterns) {
        const regex = new RegExp(pattern.pattern, 'gi');
        let match;

        while ((match = regex.exec(line)) !== null) {
          findings.push({
            pattern: pattern.name,
            category: pattern.category,
            severity: pattern.severity,
            line: lineNum + 1,
            column: match.index + 1,
            message: pattern.description,
            matchedText: match[0],
          });
        }
      }
    }

    return findings;
  }

  /**
   * Evaluate quality gates based on findings
   */
  private evaluateQualityGates(findings: AtheonFinding[]): boolean {
    if (!this.atheonConfig.qualityGates?.enabled) return true;

    const strict = this.atheonConfig.qualityGates.strict;
    const allowedFindings = this.atheonConfig.qualityGates.allowedFindings || 0;

    if (strict) {
      // Strict mode: no findings allowed
      return findings.length === 0;
    } else {
      // Lenient mode: allowed number of findings
      return findings.length <= allowedFindings;
    }
  }

  /**
   * Get Atheon-specific MCP tools
   */
  private getAtheonTools(): MCPTool[] {
    return [
      {
        name: 'scan_with_atheon',
        description: 'Scan text for patterns using Atheon',
        inputSchema: {
          type: 'object',
          properties: {
            text: {
              type: 'string',
              description: 'Text to scan',
            },
            categories: {
              type: 'array',
              items: { type: 'string' },
              description: 'Pattern categories to scan for',
            },
          },
          required: ['text'],
        },
        handler: async (input) => {
          const findings = await this.scanWithAtheon(input.text);
          return {
            findings,
            count: findings.length,
            severity_counts: this.groupFindingsBySeverity(findings),
          };
        },
      },
      {
        name: 'check_quality_gates',
        description: 'Check if content passes quality gates',
        inputSchema: {
          type: 'object',
          properties: {
            text: {
              type: 'string',
              description: 'Text to check',
            },
          },
          required: ['text'],
        },
        handler: async (input) => {
          const findings = await this.scanWithAtheon(input.text);
          const passed = this.evaluateQualityGates(findings);

          return {
            passed,
            findings,
            violations: findings.length,
          };
        },
      },
      {
        name: 'get_available_patterns',
        description: 'Get available Atheon patterns',
        inputSchema: {
          type: 'object',
          properties: {},
        },
        handler: async () => {
          return {
            patterns: ATHEON_PATTERNS,
            categories: ATHEON_CATEGORIES,
          };
        },
      },
    ];
  }

  /**
   * Group findings by severity
   */
  private groupFindingsBySeverity(findings: AtheonFinding[]): Record<string, number> {
    return findings.reduce((acc, finding) => {
      acc[finding.severity] = (acc[finding.severity] || 0) + 1;
      return acc;
    }, {} as Record<string, number>);
  }

  /**
   * Get scan results for a specific test case
   */
  getScanResults(testCaseId: string): AtheonFinding[] {
    return this.scanResults.get(testCaseId) || [];
  }

  /**
   * Get all scan results
   */
  getAllScanResults(): Map<string, AtheonFinding[]> {
    return new Map(this.scanResults);
  }

  /**
   * Clear scan results
   */
  clearScanResults(): void {
    this.scanResults.clear();
  }

  /**
   * Get Atheon configuration
   */
  getAtheonConfig(): AtheonIntegrationConfig {
    return { ...this.atheonConfig };
  }
}

// Factory function for creating Atheon Claude clients
export function createAtheonClaudeClient(config: AtheonIntegrationConfig): AtheonClaudeClient {
  return new AtheonClaudeClient(config);
}

