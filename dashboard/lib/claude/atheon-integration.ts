export interface AtheonPattern {
  name: string;
  category: string;
  severity: 'critical' | 'high' | 'medium' | 'low';
  pattern: string;
  description: string;
}

export interface AtheonFinding {
  pattern: string;
  category: string;
  severity: 'critical' | 'high' | 'medium' | 'low';
  line: number;
  column: number;
  message: string;
  matchedText: string;
}

export interface AtheonConfig {
  enabled: boolean;
  categories?: string[];
  severity?: ('critical' | 'high' | 'medium' | 'low')[];
  includeDisabled?: boolean;
}

export interface AtheonIntegrationConfig extends MCPIntegrationConfig {
  atheon: AtheonConfig;
  qualityGates?: {
    enabled: boolean;
    strict: boolean;
    allowedFindings?: number;
  };
}

