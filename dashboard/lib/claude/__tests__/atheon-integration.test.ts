/**
 * Atheon Claude Integration Unit Tests
 * Tests for Claude with Atheon pattern matching via MCP server
 */

import { describe, it, expect, beforeEach, afterEach, jest } from '@jest/globals';
import {
  AtheonClaudeClient,
  createAtheonClaudeClient,
  ATHEON_PATTERNS,
  ATHEON_CATEGORIES,
  DEFAULT_ATHEON_CONFIG,
  DEFAULT_QUALITY_GATES,
  AtheonIntegrationConfig,
  AtheonFinding
} from '../atheon-integration';
import { ClaudeMessage } from '../client';

// Mock global fetch
const mockFetch = jest.fn();
global.fetch = mockFetch as any;

describe('AtheonClaudeClient', () => {
  let client: AtheonClaudeClient;
  let config: AtheonIntegrationConfig;

  beforeEach(() => {
    mockFetch.mockClear();
    jest.spyOn(console, 'warn').mockImplementation(() => {});

    config = {
      apiKey: 'test-key',
      mcpServers: [],
      atheon: {
        enabled: true,
        categories: [...ATHEON_CATEGORIES],
        severity: ['critical', 'high', 'medium', 'low'],
      }
    };
    client = new AtheonClaudeClient(config);
  });

  afterEach(() => {
    jest.restoreAllMocks();
  });

  describe('Constructor', () => {
    it('should create client', () => {
      const atheonClient = new AtheonClaudeClient(config);
      expect(atheonClient).toBeDefined();
    });

    it('should expose atheon config', () => {
      const atheonConfig = client.getAtheonConfig();
      expect(atheonConfig.atheon).toBeDefined();
    });

    it('should return copy of config', () => {
      const c1 = client.getAtheonConfig();
      const c2 = client.getAtheonConfig();
      expect(c1).not.toBe(c2);
      expect(c1.atheon).toEqual(c2.atheon);
    });
  });

  describe('scanWithAtheon', () => {
    it('should return no findings for clean text', async () => {
      const findings = await client.scanWithAtheon('This is clean code with no issues.');
      expect(findings).toEqual([]);
    });

    it('should detect AWS access key', async () => {
      const findings = await client.scanWithAtheon('AKIAIOSFODNN7EXAMPLE is a key');
      expect(findings.length).toBeGreaterThan(0);
      expect(findings.some(f => f.pattern === 'aws-access-key')).toBe(true);
    });

    it('should detect generic API key', async () => {
      const findings = await client.scanWithAtheon('api_key = "abcdef1234567890abcdef1234567890"');
      expect(findings.some(f => f.pattern === 'api-key-generic')).toBe(true);
    });

    it('should detect console.log', async () => {
      const findings = await client.scanWithAtheon('console.log("debug")');
      expect(findings.some(f => f.pattern === 'console-log')).toBe(true);
    });

    it('should detect TODO comments', async () => {
      const findings = await client.scanWithAtheon('// TODO: fix this');
      expect(findings.some(f => f.pattern === 'todo-comment')).toBe(true);
    });

    it('should detect debugger statements', async () => {
      const findings = await client.scanWithAtheon('debugger;');
      expect(findings.some(f => f.pattern === 'debug-statement')).toBe(true);
    });

    it('should detect test skips', async () => {
      const findings = await client.scanWithAtheon('describe.skip("test", () => {})');
      expect(findings.some(f => f.pattern === 'test-skip')).toBe(true);
    });

    it('should detect AI-generated content markers', async () => {
      const findings = await client.scanWithAtheon('This was generated by AI');
      expect(findings.some(f => f.pattern === 'ai-generated')).toBe(true);
    });

    it('should include line and column information', async () => {
      const findings = await client.scanWithAtheon('AKIAIOSFODNN7EXAMPLE');
      expect(findings.length).toBeGreaterThan(0);
      expect(findings[0].line).toBe(1);
      expect(findings[0].column).toBeGreaterThan(0);
    });

    it('should include matched text', async () => {
      const findings = await client.scanWithAtheon('console.log("test")');
      expect(findings[0].matchedText).toContain('console.log');
    });

    it('should handle multi-line text', async () => {
      const text = 'line 1\nconsole.log("a")\nline 3\ndebugger;\nline 5';
      const findings = await client.scanWithAtheon(text);
      expect(findings.length).toBeGreaterThan(0);
      // Console.log should be on line 2, debugger on line 4
      const consoleLog = findings.find(f => f.pattern === 'console-log');
      const debuggerStmt = findings.find(f => f.pattern === 'debug-statement');
      expect(consoleLog?.line).toBe(2);
      expect(debuggerStmt?.line).toBe(4);
    });

    it('should return no findings when atheon is disabled', async () => {
      const disabledClient = new AtheonClaudeClient({
        ...config,
        atheon: { enabled: false }
      });
      const findings = await disabledClient.scanWithAtheon('console.log("test")');
      expect(findings).toEqual([]);
    });

    it('should filter by category', async () => {
      const filteredClient = new AtheonClaudeClient({
        ...config,
        atheon: {
          enabled: true,
          categories: ['secrets']
        }
      });
      const findings = await filteredClient.scanWithAtheon('console.log("x") AKIAIOSFODNN7EXAMPLE');
      // Should only find secrets, not console.log
      expect(findings.some(f => f.pattern === 'aws-access-key')).toBe(true);
      expect(findings.some(f => f.pattern === 'console-log')).toBe(false);
    });

    it('should filter by severity', async () => {
      const filteredClient = new AtheonClaudeClient({
        ...config,
        atheon: {
          enabled: true,
          severity: ['critical']
        }
      });
      const findings = await filteredClient.scanWithAtheon('console.log("x") AKIAIOSFODNN7EXAMPLE');
      // Only critical findings
      expect(findings.every(f => f.severity === 'critical')).toBe(true);
    });

    it('should detect multiple findings in same line', async () => {
      const findings = await client.scanWithAtheon('console.log("debug") TODO: fix this');
      expect(findings.length).toBeGreaterThanOrEqual(2);
    });
  });

  describe('executeWithAtheon', () => {
    it('should execute and scan output', async () => {
      mockFetch.mockResolvedValueOnce({
        ok: true,
        json: async () => ({
          id: 'msg_123',
          type: 'message',
          role: 'assistant',
          content: [{ type: 'text', text: 'This is clean output' }],
          model: 'claude-3-5-sonnet-20241022',
          stop_reason: 'end_turn',
          stop_sequence: null,
          usage: { input_tokens: 10, output_tokens: 20 }
        })
      } as Response);

      const messages: ClaudeMessage[] = [{ role: 'user', content: 'Hello' }];
      const result = await client.executeWithAtheon(messages);

      expect(result.response).toBeDefined();
      expect(result.findings).toBeDefined();
      expect(result.passedQualityGates).toBe(true);
    });

    it('should detect findings in scanned output', async () => {
      mockFetch.mockResolvedValueOnce({
        ok: true,
        json: async () => ({
          id: 'msg_123',
          type: 'message',
          role: 'assistant',
          content: [{ type: 'text', text: 'Here is code: console.log("debug")' }],
          model: 'claude-3-5-sonnet-20241022',
          stop_reason: 'end_turn',
          stop_sequence: null,
          usage: { input_tokens: 10, output_tokens: 20 }
        })
      } as Response);

      const messages: ClaudeMessage[] = [{ role: 'user', content: 'Generate code' }];
      const result = await client.executeWithAtheon(messages);

      expect(result.findings.length).toBeGreaterThan(0);
    });

    it('should skip scanning when scanOutput is false', async () => {
      mockFetch.mockResolvedValueOnce({
        ok: true,
        json: async () => ({
          id: 'msg_123',
          type: 'message',
          role: 'assistant',
          content: [{ type: 'text', text: 'console.log("x")' }],
          model: 'claude-3-5-sonnet-20241022',
          stop_reason: 'end_turn',
          stop_sequence: null,
          usage: { input_tokens: 10, output_tokens: 20 }
        })
      } as Response);

      const messages: ClaudeMessage[] = [{ role: 'user', content: 'Test' }];
      const result = await client.executeWithAtheon(messages, false);

      expect(result.findings).toEqual([]);
    });

    it('should apply quality gates when enabled', async () => {
      mockFetch.mockResolvedValueOnce({
        ok: true,
        json: async () => ({
          id: 'msg_123',
          type: 'message',
          role: 'assistant',
          content: [{ type: 'text', text: 'console.log("x")' }],
          model: 'claude-3-5-sonnet-20241022',
          stop_reason: 'end_turn',
          stop_sequence: null,
          usage: { input_tokens: 10, output_tokens: 20 }
        })
      } as Response);

      const strictClient = new AtheonClaudeClient({
        ...config,
        qualityGates: { enabled: true, strict: true }
      });

      const messages: ClaudeMessage[] = [{ role: 'user', content: 'Test' }];
      const result = await strictClient.executeWithAtheon(messages);

      expect(result.passedQualityGates).toBe(false);
    });
  });

  describe('runBenchmarkWithAtheon', () => {
    it('should run benchmark with Atheon integration', async () => {
      mockFetch.mockResolvedValueOnce({
        ok: true,
        json: async () => ({
          id: 'msg_123',
          type: 'message',
          role: 'assistant',
          content: [{ type: 'text', text: 'Clean output' }],
          model: 'claude-3-5-sonnet-20241022',
          stop_reason: 'end_turn',
          stop_sequence: null,
          usage: { input_tokens: 10, output_tokens: 20 }
        })
      } as Response);

      const result = await client.runBenchmarkWithAtheon(
        'bench-1',
        'test-1',
        'Test Name',
        'Test prompt',
        'System prompt'
      );

      expect(result.benchmarkId).toBe('bench-1');
      expect(result.configuration).toBe('atheon');
      expect(result.findings).toBeDefined();
      expect(result.passedQualityGates).toBeDefined();
    });

    it('should work without system prompt', async () => {
      mockFetch.mockResolvedValueOnce({
        ok: true,
        json: async () => ({
          id: 'msg_123',
          type: 'message',
          role: 'assistant',
          content: [{ type: 'text', text: 'Output' }],
          model: 'claude-3-5-sonnet-20241022',
          stop_reason: 'end_turn',
          stop_sequence: null,
          usage: { input_tokens: 5, output_tokens: 5 }
        })
      } as Response);

      const result = await client.runBenchmarkWithAtheon('b', 't', 'n', 'p');

      expect(result.configuration).toBe('atheon');
    });

    it('should handle benchmark failures', async () => {
      mockFetch.mockRejectedValueOnce(new Error('API error'));

      const result = await client.runBenchmarkWithAtheon('b', 't', 'n', 'p');

      expect(result.passed).toBe(false);
      expect(result.error).toBeDefined();
      expect(result.findings).toEqual([]);
    });

    it('should store scan results', async () => {
      mockFetch.mockResolvedValueOnce({
        ok: true,
        json: async () => ({
          id: 'msg_123',
          type: 'message',
          role: 'assistant',
          content: [{ type: 'text', text: 'console.log("test")' }],
          model: 'claude-3-5-sonnet-20241022',
          stop_reason: 'end_turn',
          stop_sequence: null,
          usage: { input_tokens: 10, output_tokens: 20 }
        })
      } as Response);

      await client.runBenchmarkWithAtheon('b', 'test-1', 'n', 'p');

      const results = client.getScanResults('test-1');
      expect(results).toBeDefined();
      expect(Array.isArray(results)).toBe(true);
    });
  });

  describe('Scan Results Management', () => {
    it('should return empty array for unknown test case', () => {
      const results = client.getScanResults('nonexistent');
      expect(results).toEqual([]);
    });

    it('should return copy of all scan results', () => {
      const all1 = client.getAllScanResults();
      const all2 = client.getAllScanResults();
      expect(all1).not.toBe(all2);
    });

    it('should clear all scan results', () => {
      client.clearScanResults();
      const all = client.getAllScanResults();
      expect(all.size).toBe(0);
    });
  });
});

